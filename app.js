const $ = (selector) => document.querySelector(selector);
const byValueDesc = (a, b) => b[1] - a[1];

function initKpis() {
  const grid = $('#kpiGrid');
  const highlight = ['Tổng nhân sự', 'Thành viên mới (Hiện hữu)', 'Lãnh đạo', 'Nhân viên', 'Nam', 'Nữ', 'Độ tuổi từ 20 tuổi - 35 tuổi', 'Độ tuổi trên 35 tuổi'];
  grid.innerHTML = DATA.summary
    .filter(item => highlight.includes(item.label))
    .map(item => `<article class="kpi"><span>${item.label}</span><strong>${item.value}</strong></article>`)
    .join('');
  const total = DATA.summary.find(item => item.label === 'Tổng nhân sự');
  $('#heroTotal').textContent = total ? total.value : DATA.employees.length;
}

function countBy(key) {
  return DATA.employees.reduce((acc, item) => {
    const value = item[key] || 'Khác';
    acc[value] = (acc[value] || 0) + 1;
    return acc;
  }, {});
}

function drawGenderChart() {
  const canvas = $('#genderChart');
  const ctx = canvas.getContext('2d');
  const gender = {
    "Nam": DATA.summary.find(item => item.label === 'Nam')?.value || 0,
    "Nữ": DATA.summary.find(item => item.label === 'Nữ')?.value || 0
  };
  const total = Object.values(gender).reduce((a, b) => a + b, 0);
  const items = Object.entries(gender);
  const colors = ['#2563eb', '#38bdf8', '#94a3b8'];
  let start = -Math.PI / 2;
  ctx.clearRect(0, 0, canvas.width, canvas.height);
  const cx = canvas.width / 2, cy = 95, radius = 72;

  items.forEach(([label, value], index) => {
    const angle = (value / total) * Math.PI * 2;
    ctx.beginPath();
    ctx.moveTo(cx, cy);
    ctx.arc(cx, cy, radius, start, start + angle);
    ctx.fillStyle = colors[index % colors.length];
    ctx.fill();
    start += angle;
  });

  ctx.fillStyle = '#172033';
  ctx.font = '600 14px Arial';
  items.forEach(([label, value], index) => {
    const x = 30 + index * 120;
    ctx.fillStyle = colors[index % colors.length];
    ctx.fillRect(x, 185, 14, 14);
    ctx.fillStyle = '#172033';
    ctx.fillText(`${label}: ${value}`, x + 22, 197);
  });
}

function renderDepartmentBars() {
  const departments = Object.entries(countBy('department')).sort(byValueDesc);
  const max = Math.max(...departments.map(item => item[1]));
  $('#departmentBars').innerHTML = departments.map(([name, value]) => `
    <div class="bar-row">
      <span title="${name}">${name}</span>
      <div class="bar-track"><div class="bar-fill" style="width:${(value / max) * 100}%"></div></div>
      <strong>${value}</strong>
    </div>
  `).join('');
}

function renderLocations() {
  $('#locationCards').innerHTML = Object.entries(countBy('location')).sort(byValueDesc).map(([name, value]) => `
    <div class="location-item"><span>${name}</span><strong>${value}</strong><small>nhân sự</small></div>
  `).join('');
}

function fillFilters() {
  const departmentSelect = $('#departmentFilter');
  const locationSelect = $('#locationFilter');
  [...new Set(DATA.employees.map(e => e.department).filter(Boolean))].sort().forEach(dep => departmentSelect.innerHTML += `<option value="${dep}">${dep}</option>`);
  [...new Set(DATA.employees.map(e => e.location).filter(Boolean))].sort().forEach(loc => locationSelect.innerHTML += `<option value="${loc}">${loc}</option>`);
}

function renderTable() {
  const keyword = $('#searchInput').value.trim().toLowerCase();
  const department = $('#departmentFilter').value;
  const location = $('#locationFilter').value;
  const rows = DATA.employees.filter(item => {
    const text = `${item.code} ${item.name} ${item.department} ${item.position}`.toLowerCase();
    return (!keyword || text.includes(keyword)) && 
           (!department || item.department.toLowerCase() === department.toLowerCase()) && 
           (!location || item.location.toLowerCase() === location.toLowerCase());
  });

  $('#tableCount').textContent = `${rows.length} nhân sự`;
  $('#employeeTable').innerHTML = rows.map(item => `
    <tr>
      <td>${item.stt}</td><td>${item.code}</td><td><strong>${item.name}</strong></td>
      <td>${item.department}</td><td>${item.position}</td><td>${item.gender}</td><td>${item.location}</td>
    </tr>
  `).join('');
}

function formatPlanAction(text) {
  let html = '';
  let inList = false;
  text.split('\n').forEach(line => {
    line = line.trim();
    if (!line) return;
    if (line.startsWith('*') || line.startsWith('-')) {
      if (!inList) { html += '<ul style="padding-left: 20px; margin: 8px 0; color: #475569; line-height: 1.6;">'; inList = true; }
      html += `<li style="margin-bottom: 4px;">${line.substring(1).trim()}</li>`;
    } else {
      if (inList) { html += '</ul>'; inList = false; }
      html += `<div style="font-weight: 600; color: #334155; margin-top: 8px;">${line}</div>`;
    }
  });
  if (inList) html += '</ul>';
  return html;
}

function renderPlans() {
  $('#reportNote').textContent = DATA.note;
  $('#planList').innerHTML = DATA.plans.map(plan => `
    <article class="plan">
      <div class="plan-number">${plan.stt}</div>
      <div><h4>${plan.title}</h4><div class="plan-desc" style="font-size: 14px;">${formatPlanAction(plan.action)}</div></div>
    </article>
  `).join('');
}

function initScrollAnimations() {
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('active');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.1, rootMargin: "0px 0px -50px 0px" });

  document.querySelectorAll('.reveal').forEach(el => observer.observe(el));
}

function boot() {
  initKpis();
  drawGenderChart();
  renderDepartmentBars();
  renderLocations();
  fillFilters();
  renderTable();
  renderPlans();
  initScrollAnimations();
  ['#searchInput', '#departmentFilter', '#locationFilter'].forEach(selector => $(selector).addEventListener('input', renderTable));
}

boot();
